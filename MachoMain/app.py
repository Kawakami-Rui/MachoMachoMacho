from flask import Flask , render_template

# ==================================================
# インスタンス生成
# ==================================================
app = Flask(__name__)

# ==================================================
# ルーティング
# ==================================================
@app.route('/')
def hello_world():
    return render_template('index.html')

# ==================================================
# 実行
# ==================================================
if __name__ == '__main__':
    app.run(port=5001, debug=True)